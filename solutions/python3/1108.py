
class Solution:
    # 定义一个方法用于将IP地址中的点替换为"[.]"
    def defangIPaddr(self, address: str) -> str:
        # 使用replace方法将所有"."替换成"[.]"
        return address.replace(".", "[.]")



class Solution:
    # Define a method to replace all "." in the IP address with "[.]"
    def defangIPaddr(self, address: str) -> str:
        # Use the replace method to substitute all "." with "[.]"
        return address.replace(".", "[.]")
