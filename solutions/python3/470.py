
class Solution:
    # 返回一个1到10之间的随机整数（包含1和10）
    # 中文注释：返回一个1到10之间的随机整数（包含1和10）

    def rand10(self):
        # 通过多次调用rand7()生成一个范围在[0,63]之间的随机数
        # 使用生成的随机数映射到[1,10]范围内
        # 中文注释：通过多次调用rand7()生成一个范围在[0,63]之间的随机数，使用生成的随机数映射到[1,10]范围内
        
        while True:
            num = sum(rand7() for _ in range(10)) % 64
            if num < 60:  # 60是大于等于10的最大整数，确保结果在[1,10]
                return (num % 10) + 1
